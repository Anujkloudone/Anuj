// Go program to illustrate the
// use of simple for loop
package main

import "fmt"

// Main function
func main() {

	// for loop
	// This loop starts when i = 0
	// executes till i<4 condition is true
	// post statement is i++
	for i := 0; i < 4; i++ { // if we are remving all the codition then it will be infintie loop
		fmt.Printf("GeeksforGeeks\n")
	}

}
